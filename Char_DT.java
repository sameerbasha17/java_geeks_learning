public class Char_DT {
    public static void main(String args[])
    {
        // Understanding the Char data type
        /* Size = 2 bytes(16 bits)
         * Values= '\u0000' (0)to '\uffff' (65535)
         * Default value = '\u0000'
         */

         char a = 'a';
         char b = 's';

         System.out.println(a); // outputs a
         System.out.println(a+b); // outputs 212
    }
    
}
