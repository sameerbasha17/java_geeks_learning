public class Float_DT {
    public static void main(String args[])
    {
        // Understanding float data type
        /* Size : 4 bytes (32 bits) 
         * Values : upto 7 decimal digits
         * Default value : 0.0f
        */

      //  float a =1.43; // throughs a error if you declare without the "f" suffix
        float b = 1.143f;
        float c = 89.89f;
        int d =7;
        
      //  System.out.println(a);
        System.out.println(b+c);
        System.out.println(c+d);
        
    }
        
}
